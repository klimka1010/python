for n in range(100, 1, -1):
    s = bin(n)[2:]  # перевод в двоичную систему
    s = str(s)
    if n % 2 == 0:
        s += "10"
    else:
        s += "01"
    r = int(s, 2)  # перевод в десятичную систему
    if r <= 102:
        print(r)
        break


# Автомат обрабатывает натуральное число N по следующему алгоритму:
