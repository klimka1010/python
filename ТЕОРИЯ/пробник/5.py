# если N чётное, в конец числа (справа) дописывается 10, в противном случае
# справа дописывается 01. Например, двоичная запись 1001 числа 9 будет
# преобразована в 100101.
# Полученная таким образом запись (в ней на два разряда больше, чем в
# записи исходного числа N) является двоичной записью числа — результата
# работы данного алгоритма.

